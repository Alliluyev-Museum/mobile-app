@Composable
private fun ExhibitCard(exhibit: Exhibit) {
    val context = LocalContext.current
    Card(modifier = Modifier
        .fillMaxWidth()
        .padding(all = 8.dp)
        .clickable {
            val intent = Intent(context, ExhibitActivity::class.java)
            intent.putExtra(context.getString(R.string.exhibit_name_key), exhibit.name)
            intent.putExtra(context.getString(R.string.exhibit_description_key), exhibit.fullDescription)
            context.startActivity(intent)
        }) {
            Row {
                Image(
                    painter = painterResource(id = R.drawable.ic_launcher_background),
                    contentDescription = exhibit.name
                )
                Spacer(modifier = Modifier.padding(8.dp))
                Column(modifier = Modifier.padding(0.dp, 0.dp, 8.dp)) {
                    Text(
                        exhibit.name,
                        style = MaterialTheme.typography.headlineMedium,
                    )
                    Text(exhibit.description)
                }
            }
    }
}

@Composable
private fun ExhibitList(exhibits: List<Exhibit>) {
    LazyColumn(
        modifier = Modifier,
        state = rememberLazyListState()) {
            items(exhibits) { exhibit ->
                ExhibitCard(exhibit)
            }
    }
}

