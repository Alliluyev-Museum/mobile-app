    private suspend fun loadImages(): List<Bitmap> {
        return withContext(Dispatchers.IO) {
            filesDir
                .listFiles()
                ?.filter { it.canRead() && it.isFile && it.name.endsWith(".jpg") }
                ?.map {
                    val bytes = it.readBytes()
                    BitmapFactory.decodeByteArray(bytes, 0, bytes.size)
                } ?: listOf()
        }
    }
